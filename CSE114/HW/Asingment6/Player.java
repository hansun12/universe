public class Player {

    public int no;
    public String position;
    public String name;
    public String height;
    public int weight;
    public String hand;
    public double avg;


    Player(int no, String position, String name, String height, int weight, String hand, double avg) {
        this.no = no;
        this.position = position;
        this.name = name;
        this.height = height;
        this.weight = weight;
        this.hand = hand;
        this.avg = avg;
    }
}
